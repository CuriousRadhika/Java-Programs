import java.util.Scanner;

class squarepattern{
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for(int x=1 ; x <= n ; x++)
        {
            for(int y=1 ; y <= n ; y++)
            {
                System.out.print(n);
            }
            System.out.println("");
        }
        
    }
}
